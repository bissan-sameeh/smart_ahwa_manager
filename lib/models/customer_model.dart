class Customer{
  final int id;
  final String name;
  final String email;
  final String phone;
  final String address;
  final String city;
  final String country;
  final String postalCode;
  final String thumbnail;
  Customer({
    required this.id,
    required this.name,
    required this.email,
    required this.phone,
    required this.address,
    required this.city,
    required this.country,
    required this.postalCode,
    required this.thumbnail,
  });
}